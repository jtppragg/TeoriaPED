public class Figura {
    private String nombre;
    private int lados;
    
    String getNombre() {
        return nombre;
    }
    
    void setNombre(String nombre) {
        this.nombre = nombre;
    }
    
    int getLados() {
        return lados;
    }
    
    void setLados(int lados) {
        this.lados = lados;
    }
}
